    /// <summary>
    /// Initializes a new instance of the <see cref="ExperienceSeeder"/> class.
    /// </summary>
    /// <param name="datacenterService">The service for accessing datacenter data.</param>
    /// <param name="authDbContext">The authentication database context.</param>
    /// <param name="gameDbContext">The game database context.</param>
    public ExperienceSeeder(IDatacenterService datacenterService, AuthDbContext authDbContext, GameDbContext gameDbContext) : base(datacenterService, authDbContext, gameDbContext)
    {
    }

    /// <summary>
    /// Seeds the experience data into the database asynchronously.
    /// </summary>
    /// <param name="cancellationToken">A token to monitor for cancellation requests.</param>
    /// <returns>A task that represents the asynchronous operation.</returns>
    public override async Task SeedAsync(CancellationToken cancellationToken)
    {
        await GameDbContext.Experiences.ExecuteDeleteAsync(cancellationToken);
        GameDbContext.Experiences.AddRange(s_experiences);
        await GameDbContext.SaveChangesAsync(cancellationToken);
    }
